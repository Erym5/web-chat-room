package com.farsunset.chat.repository;

import com.farsunset.chat.entity.Member;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.domain.Specification;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.stereotype.Repository;
import org.springframework.transaction.annotation.Transactional;

import java.util.List;

@Repository
@Transactional(rollbackFor = Exception.class)
public interface MemberRepository extends JpaRepository<Member, Long> {
    Page<Member> findAll(Specification<Member> sp, Pageable pageable);

    @Modifying
    @Query("delete from Member where roomId = ?1 and uid = ?2")
    int delete(Long roomId, String uid);

    @Query("from Member where roomId = ?1 order by createTime desc")
    List<Member> findList(Long roomId);
}
